# ================================================

def run_monte_carlo(S0, annual_drift, annual_vol, shock, n_sims=1000, n_days=252):
    dt = 1 / 252
    Z  = np.random.standard_normal((n_days, n_sims))
    daily_returns = (annual_drift - 0.5 * annual_vol**2) * dt + annual_vol * np.sqrt(dt) * Z
    daily_returns[0] += shock
    log_paths   = np.cumsum(daily_returns, axis=0)
    price_paths = S0 * np.exp(log_paths)
    return np.vstack([np.full(n_sims, S0), price_paths])


