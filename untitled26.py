def fetch_apple_headlines(n=20) -> list[dict]:
    """
    Scrapes Apple news headlines from Google News RSS feed.
    No API key required — completely free.
    Returns a list of dicts with title, source, published date.
    """
    url = "https://news.google.com/rss/search?q=Apple+AAPL+stock&hl=en-US&gl=US&ceid=US:en"
    headers = {"User-Agent": "Mozilla/5.0"}

    try:
        resp = requests.get(url, headers=headers, timeout=10)
        soup = BeautifulSoup(resp.content, "xml")
        items = soup.find_all("item")[:n]

        headlines = []
        for item in items:
            title   = item.find("title").text   if item.find("title")   else ""
            source  = item.find("source").text  if item.find("source")  else "Unknown"
            pubdate = item.find("pubDate").text if item.find("pubDate") else ""
            headlines.append({"title": title, "source": source, "pubdate": pubdate})

        print(f"\n  Fetched {len(headlines)} headlines from Google News")
        return headlines

    except Exception as e:
        print(f"\n  Could not fetch live headlines ({e})")
        print("  Using sample headlines for demonstration...")
        return [
            {"title": "Apple beats earnings expectations with record services revenue", "source": "Reuters", "pubdate": ""},
            {"title": "iPhone demand in China shows signs of recovery analysts say",    "source": "Bloomberg","pubdate": ""},
            {"title": "Apple supplier warns of production delays amid trade tensions",   "source": "FT",       "pubdate": ""},
            {"title": "Apple faces regulatory scrutiny in EU over App Store policies",   "source": "WSJ",      "pubdate": ""},
            {"title": "Analysts raise Apple price target on strong services growth",     "source": "CNBC",     "pubdate": ""},
        ]


def analyse_sentiment(headlines: list[dict]) -> dict:
    """
    Runs TextBlob sentiment analysis on each headline.
    Returns:
      - per-headline scores
      - overall composite score (-1 = very negative, +1 = very positive)
      - a sentiment-adjusted shock modifier
    """
    print("\n" + "="*55)
    print("  MODULE 2: NEWS SENTIMENT ANALYSIS")
    print("="*55)

    scored = []
    for h in headlines:
        blob       = TextBlob(h["title"])
        polarity   = blob.sentiment.polarity      # -1 to +1
        subjectivity = blob.sentiment.subjectivity  # 0 to 1
        scored.append({**h, "polarity": polarity, "subjectivity": subjectivity})

    df = pd.DataFrame(scored)

    composite   = df["polarity"].mean()
    pos_count   = (df["polarity"] > 0.05).sum()
    neg_count   = (df["polarity"] < -0.05).sum()
    neu_count   = len(df) - pos_count - neg_count

    print(f"\n  Headlines analysed : {len(df)}")
    print(f"  Positive           : {pos_count}")
    print(f"  Neutral            : {neu_count}")
    print(f"  Negative           : {neg_count}")
    print(f"  Composite score    : {composite:+.3f}  (-1=very bearish, +1=very bullish)")

    # Map sentiment score to a shock modifier
    # e.g. composite of -0.3 → adds -1.5% extra shock
    sentiment_modifier = composite * 0.05
    print(f"  Sentiment modifier : {sentiment_modifier:+.2%} adjustment to shock\n")

    print("  Top headlines:")
    for _, row in df.nlargest(3, "polarity").iterrows():
        print(f"    [+{row['polarity']:.2f}] {row['title'][:75]}")
    for _, row in df.nsmallest(3, "polarity").iterrows():
        print(f"    [{row['polarity']:.2f}] {row['title'][:75]}")

    return {
        "df":                df,
        "composite":         composite,
        "pos_count":         pos_count,
        "neg_count":         neg_count,
        "neu_count":         neu_count,
        "sentiment_modifier": sentiment_modifier,
    }


