def main():
    print("\n" + "="*55)
    print("  APPLE SUPPLY CHAIN SHOCK SIMULATOR — EXTENDED")
    print("="*55)

    np.random.seed(42)

    # --- Fetch Apple baseline ---
    print("\nFetching AAPL market data...")
    data = yf.download("AAPL", period="2y", progress=False)
    close = data["Close"].squeeze()
    S0    = float(close.iloc[-1])
    log_r = np.log(close / close.shift(1)).dropna()
    vol   = float(log_r.std()   * np.sqrt(252))
    drift = float(log_r.mean()  * 252)
    print(f"  Current price : ${S0:.2f}")
    print(f"  Volatility    : {vol:.1%}")
    print(f"  Drift         : {drift:.1%}")

    # --- Module 1: Historical shock calibration ---
    shock_df         = measure_historical_shocks(HISTORICAL_SHOCKS)
    calibrated_shock = calibrate_shock(shock_df, category="supply_chain")

    # --- Module 2: Sentiment analysis ---
    headlines = fetch_apple_headlines(n=20)
    sentiment = analyse_sentiment(headlines)

    # --- Combine: calibrated shock + sentiment modifier ---
    final_shock = calibrated_shock + sentiment["sentiment_modifier"]
    print(f"\n  Final combined shock : {calibrated_shock:+.2%} (historical) "
          f"+ {sentiment['sentiment_modifier']:+.2%} (sentiment) = {final_shock:+.2%}")

    # --- Run Monte Carlo ---
    print(f"\nRunning 1,000 simulations...")
    paths = run_monte_carlo(S0, drift, vol, final_shock, n_sims=1000, n_days=252)
    print("Done.")

    # --- Plot ---
    plot_dashboard(paths, S0, shock_df, sentiment, final_shock)


if __name__ == "__main__":
    main()