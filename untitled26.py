def measure_historical_shocks(shocks: dict, ticker="AAPL") -> pd.DataFrame:
    """
    For each historical event, downloads AAPL price data
    and measures the actual % change Apple suffered.
    """
    print("\n" + "="*55)
    print("  MODULE 1: HISTORICAL SHOCK CALIBRATION")
    print("="*55)

    results = []
    for name, info in shocks.items():
        try:
            start = pd.to_datetime(info["start"]) - timedelta(days=3)
            end   = pd.to_datetime(info["end"])   + timedelta(days=3)

            data  = yf.download(ticker, start=start, end=end, progress=False)
            if data.empty or len(data) < 2:
                print(f"  [{name}] — no data available, skipping")
                continue

            close      = data["Close"].squeeze()
            price_start = float(close.iloc[0])
            price_end   = float(close.iloc[-1])
            pct_change  = (price_end - price_start) / price_start * 100
            n_days      = len(close)

            results.append({
                "event":       name,
                "category":    info["category"],
                "description": info["description"],
                "date_start":  info["start"],
                "date_end":    info["end"],
                "price_start": round(price_start, 2),
                "price_end":   round(price_end,   2),
                "pct_change":  round(pct_change,  2),
                "n_days":      n_days,
            })

            sign = "+" if pct_change >= 0 else ""
            print(f"  {name}")
            print(f"    ${price_start:.2f} → ${price_end:.2f}  |  {sign}{pct_change:.1f}%  over {n_days} trading days")

        except Exception as e:
            print(f"  [{name}] — error: {e}")

    df = pd.DataFrame(results)
    return df


def calibrate_shock(shock_df: pd.DataFrame, category: str = "supply_chain") -> float:
    """
    Filters historical shocks by category and returns the
    median % change as a calibrated shock value for the simulation.
    """
    subset = shock_df[shock_df["category"] == category]
    if subset.empty:
        print(f"\n  No historical data for category '{category}', using -3% default.")
        return -0.03

    median_shock = subset["pct_change"].median() / 100
    print(f"\n  Calibrated shock for '{category}' events : {median_shock:+.2%}")
    print(f"  (median of {len(subset)} real historical events)")
    return median_shock


